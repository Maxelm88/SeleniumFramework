package helpers.throwables;

public class FalseActionExecutionException extends RuntimeException {

    public FalseActionExecutionException() {super();}

    public FalseActionExecutionException(String message) {super(message);}

    public FalseActionExecutionException(Throwable cause) {super(cause);}

    public FalseActionExecutionException(String message, Throwable cause) {super(message, cause);}
}
